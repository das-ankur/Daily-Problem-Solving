class Solution {
  public:
    string armstrongNumber(int n){
        // code here
        int temp=n;
        int arm_num=0;
        while(temp>0) {
            int digit=temp%10;
            temp/=10;
            arm_num+=pow(digit,3);
        }
        return arm_num==n?"Yes":"No";
    }
};

